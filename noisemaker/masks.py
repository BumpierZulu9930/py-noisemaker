def iching(x, y, row, shape, **kwargs):
    height = shape[0]
    width = shape[1]

    if any(n == 0 for n in (x % width, y % height)):
        return 0

    if any(n == 1 for n in (width - (x % width), height - (y % height))):
        return 0

    if y % 2 == 0:
        return 0

    if x % 2 == 1 and x % width not in (3, 4):
        return 1

    if x % 2 == 0:
        return row[x - 1]

    return random.randint(0, 1)


